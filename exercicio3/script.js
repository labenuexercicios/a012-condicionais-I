let nacionalidade = prompt("Qual é a sua nacionalidade?")

if (nacionalidade === "brasileira"){alert ("Você é brasileiro")}
else if (nacionalidade === "argentina"){alert ("Você é argentino")}
else if (nacionalidade === "uruguaia"){alert ("Você é uruguaio")}
else if (nacionalidade === "chilena"){alert ("Você é chileno")}
else if (nacionalidade === "colombiana"){alert ("Você é colombiano")}
else {alert ("Nacionalidade não encontrada")}